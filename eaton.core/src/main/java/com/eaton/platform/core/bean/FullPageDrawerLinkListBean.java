package com.eaton.platform.core.bean;

/**
 * <html> Description: This bean class used in FullPageDrawerHelper class to store content </html> 
 * @author TCS
 * @version 1.0
 * @since 2017
 *
 */
public class FullPageDrawerLinkListBean {
	
	 private String linkPath;
	 private String transLinkTitle;
	 private String icons;
	 
	/**
	 * @return the linkPath
	 */
	public String getLinkPath() {
		return linkPath;
	}
	/**
	 * @param linkPath the linkPath to set
	 */
	public void setLinkPath(String linkPath) {
		this.linkPath = linkPath;
	}
	/**
	 * @return the transLinkTitle
	 */
	public String getTransLinkTitle() {
		return transLinkTitle;
	}
	/**
	 * @param transLinkTitle the transLinkTitle to set
	 */
	public void setTransLinkTitle(String transLinkTitle) {
		this.transLinkTitle = transLinkTitle;
	}
	/**
	 * @return the icons
	 */
	public String getIcons() {
		return icons;
	}
	/**
	 * @param icons the icons to set
	 */
	public void setIcons(String icons) {
		this.icons = icons;
	}
	 
	 

}
